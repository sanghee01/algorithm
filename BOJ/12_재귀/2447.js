function printStar(n) {
  if (n < 3) return;
  for (let i = 0; i < n; i++) {
    for (let j = 0; j < n; j++) {
      if (i >= n / 3 && i < n - n / 3 && j >= n / 3 && j < n - n / 3) {
        process.stdout.write(" ");
      } else {
        process.stdout.write("*");
      }
    }
    console.log();
    return printStar(n / 3);
  }
}

